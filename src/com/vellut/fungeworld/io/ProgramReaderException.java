package com.vellut.fungeworld.io;

public class ProgramReaderException extends Exception {

	public ProgramReaderException() {
		super();
	}

	public ProgramReaderException(String message, Throwable cause) {
		super(message, cause);
	}

	public ProgramReaderException(String message) {
		super(message);
	}

	public ProgramReaderException(Throwable cause) {
		super(cause);
	}

}
